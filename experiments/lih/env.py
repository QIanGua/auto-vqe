import numpy as np
import sys
import os
import json
from typing import Any, Dict, List, Tuple, Optional

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '../../')))
from core.base_env import QuantumEnvironment


def get_exact_from_paulis(paulis, n_qubits):
    X = np.array([[0, 1], [1, 0]])
    Y = np.array([[0, -1j], [1j, 0]])
    Z = np.array([[1, 0], [0, -1]])
    I = np.array([[1, 0], [0, 1]])

    def tensor_op(ops_list):
        res = 1
        current_ops = {idx: op for op, idx in ops_list}
        for i in range(n_qubits):
            op_type = current_ops.get(i, 'i')
            if op_type == 'x': op = X
            elif op_type == 'y': op = Y
            elif op_type == 'z': op = Z
            else: op = I
            res = np.kron(res, op)
        return res

    H = np.zeros((2**n_qubits, 2**n_qubits), dtype=complex)
    for coeff, ops in paulis:
        if not ops:
            H += coeff * np.eye(2**n_qubits)
        else:
            H += coeff * tensor_op(ops)
            
    eigenvalues = np.linalg.eigvalsh(H)
    return np.min(eigenvalues)


_PYS_CF_DATA_CACHE: Optional[Dict[str, Any]] = None


def _load_pyscf_data() -> Optional[Dict[str, Any]]:
    """
    Try to load pre-generated LiH data from PySCF / OpenFermion.

    The data file is generated by `pyscf_generate_lih_data.py` and lives
    next to this module as `lih_pyscf_data.json`.
    """
    global _PYS_CF_DATA_CACHE
    if _PYS_CF_DATA_CACHE is not None:
        return _PYS_CF_DATA_CACHE

    data_path = os.path.join(os.path.dirname(__file__), "lih_pyscf_data.json")
    if not os.path.exists(data_path):
        return None

    with open(data_path, "r") as f:
        data = json.load(f)
    _PYS_CF_DATA_CACHE = data
    return data


def _find_point_for_R(R: float) -> Optional[Dict[str, Any]]:
    """
    Given a bond length R (Angstrom), find the closest entry in the
    pre-generated PySCF data, if available.
    """
    data = _load_pyscf_data()
    if data is None:
        return None
    pts = data.get("points", [])
    if not pts:
        return None

    # Find the point with minimal |R_i - R|
    best = None
    best_dist = float("inf")
    for pt in pts:
        R_i = float(pt.get("R"))
        d = abs(R_i - R)
        if d < best_dist:
            best_dist = d
            best = pt
    return best


def _get_default_point() -> Optional[Dict[str, Any]]:
    """
    Pick a default geometry from the PySCF data.
    Strategy: choose the point with minimal exact energy (typically near
    equilibrium bond length).
    """
    data = _load_pyscf_data()
    if data is None:
        return None
    pts = data.get("points", [])
    if not pts:
        return None
    return min(pts, key=lambda pt: float(pt.get("exact_energy")))


class LiHEnvironment(QuantumEnvironment):
    """
    LiH environment that can optionally be parameterized by bond length.

    Preferred usage for geometry scan:
        env = LiHEnvironment(R=1.6)  # R in Angstrom

    This will look for a PySCF-generated data point at that geometry.

    For backward compatibility, calling `LiHEnvironment()` without
    arguments falls back to:
        - PySCF default point with minimal energy, if data exists;
        - otherwise, the historical hard-coded toy Hamiltonian used in
          early experiments.
    """

    def __init__(self, R: Optional[float] = None):
        data_point: Optional[Dict[str, Any]] = None

        if R is not None:
            data_point = _find_point_for_R(R)
        if data_point is None:
            data_point = _get_default_point()

        if data_point is not None:
            n_qubits = int(data_point.get("n_qubits", 4))
            # JSON format: "paulis": [{"coeff": ..., "ops": [["z", 0], ...]}, ...]
            raw_paulis = data_point.get("paulis", [])
            paulis: List[Tuple[float, List[Tuple[str, int]]]] = []
            for term in raw_paulis:
                coeff = float(term["coeff"])
                ops_list = []
                for op_type, idx in term.get("ops", []):
                    ops_list.append((str(op_type).lower(), int(idx)))
                paulis.append((coeff, ops_list))
            self.paulis = paulis

            # full FCI (electronic + nuclear) from PySCF on the original
            # molecule (before active-space reduction)
            self.full_fci_energy = float(data_point.get("exact_energy"))

            # active-space "exact" energy: diagonalize the 4-qubit
            # Hamiltonian defined by self.paulis. This is what we want to
            # use when评估 ansatz 对当前 4-qubit 模型的逼近误差。
            exact_active = get_exact_from_paulis(self.paulis, n_qubits).real
            self.exact_energy_active = exact_active

            # QuantumEnvironment.exact_energy 指向 active-space exact，
            # 这样 VQE 层面的 energy_error 代表纯粹的 ansatz 误差。
            super().__init__(f"LiH_R_{data_point.get('R', 'unknown')}", n_qubits, exact_active)
        else:
            # Fallback to the original toy Hamiltonian if no PySCF data is
            # available. This keeps older experiments runnable even if the
            # user has not yet generated `lih_pyscf_data.json`.
            super().__init__("LiH_Full", 4, -7.8827)
            self.paulis = [
                (-2.4270, []),  # Constant
                (0.132221, [('z', 0)]),
                (0.132221, [('z', 1)]),
                (-0.012341, [('z', 2)]),
                (-0.012341, [('z', 3)]),
                (0.170241, [('z', 0), ('z', 1)]),
                (0.121401, [('z', 0), ('z', 2)]),
                (0.168321, [('z', 0), ('z', 3)]),
                (0.168321, [('z', 1), ('z', 2)]),
                (0.121401, [('z', 1), ('z', 3)]),
                (0.174321, [('z', 2), ('z', 3)]),
                (0.045321, [('x', 0), ('x', 1), ('y', 2), ('y', 3)]),
                (0.045321, [('y', 0), ('y', 1), ('x', 2), ('x', 3)]),
                (0.045321, [('x', 0), ('y', 1), ('y', 2), ('x', 3)]),
                (0.045321, [('y', 0), ('x', 1), ('x', 2), ('y', 3)]),
                (0.018121, [('z', 0), ('z', 1), ('z', 2)]),
                (0.018121, [('z', 0), ('z', 1), ('z', 3)]),
            ]
            # Derive an internal "exact energy" from this toy model.
            self.exact_energy = get_exact_from_paulis(self.paulis, self.n_qubits).real

    def compute_energy(self, c):
        import tensorcircuit as tc
        energy = 0.0
        for coeff, ops in self.paulis:
            if not ops:
                energy += coeff
            else:
                energy += coeff * c.expectation(*[[getattr(tc.gates, op_type)(), [idx]] for op_type, idx in ops])
        return tc.backend.real(energy)

ENV = LiHEnvironment()
